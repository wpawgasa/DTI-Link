package dti.tdl.messaging;

/**
 *
 * @author Administrator
 */
public class UIControlMessage {
    public String control_name;
    public String control_messages;

    public UIControlMessage() {
    }

    public String getControl_name() {
        return control_name;
    }

    public void setControl_name(String control_name) {
        this.control_name = control_name;
    }

    public String getControl_messages() {
        return control_messages;
    }

    public void setControl_messages(String control_messages) {
        this.control_messages = control_messages;
    }
    
    
}
